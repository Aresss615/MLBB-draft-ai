export function getHeroesByRole(role: string): Hero[] {
  return heroes.filter(h => h.primaryRole === role || h.secondaryRole === role);
}

export function getAllUsedIds(draft: {
  allyPicks: { heroId: string | null }[];
  enemyPicks: { heroId: string | null }[];
  allyBans: (string | null)[];
  enemyBans: (string | null)[];
}): Set<string> {
  const ids = new Set<string>();
  draft.allyPicks.forEach(s => s.heroId && ids.add(s.heroId));
  draft.enemyPicks.forEach(s => s.heroId && ids.add(s.heroId));
  draft.allyBans.forEach(id => id && ids.add(id));
  draft.enemyBans.forEach(id => id && ids.add(id));
  return ids;
}
